# The modulus operator in Python, denoted by the percent sign %, calculates the remainder of a division operation. 
# It returns the value left over after dividing one number (the dividend) by another (the divisor).

# Examples
# 7 % 3 results in 1 because 7 divided by 3 is 2 with a remainder of 1.
# 10 % 2 results in 0 because 10 is perfectly divisible by 2.
# 5 % 7 results in 5 because 5 divided by 7 is 0 with a remainder of 5.
# 10.5 % 3 results in 1.5 because the modulo operation can be used with floating-point numbers.

